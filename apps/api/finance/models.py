import uuid
from django.db import models
from django.utils import timezone
from decimal import Decimal


class FeeType(models.Model):
    """Types of fees"""
    
    class FeeCategory(models.TextChoices):
        TUITION = 'tuition', 'Tuition'
        ACADEMIC = 'academic', 'Academic'
        HOSTEL = 'hostel', 'Hostel'
        MEDICAL = 'medical', 'Medical'
        ICT = 'ict', 'ICT'
        LIBRARY = 'library', 'Library'
        REGISTRATION = 'registration', 'Registration'
        OTHER = 'other', 'Other'
    
    id = models.UUIDField(primary_key=True, default=uuid.uuid4, editable=False)
    
    university = models.ForeignKey('university.University', on_delete=models.CASCADE, related_name='fee_types')
    
    name = models.CharField(max_length=100)
    category = models.CharField(max_length=20, choices=FeeCategory.choices)
    
    # Amount
    amount = models.DecimalField(max_digits=12, decimal_places=2)
    
    # Applicable levels
    applicable_levels = models.JSONField(default=list)  # List of levels
    
    # System-specific
    is_mandatory = models.BooleanField(default=True)
    is_recurring = models.BooleanField(default=True)
    
    # For Nigerian: TETFund levy
    tetfund_levy = models.BooleanField(default=False)
    tetfund_percentage = models.FloatField(default=0)
    
    is_active = models.BooleanField(default=True)
    
    class Meta:
        db_table = 'fee_types'
    
    def __str__(self):
        return f"{self.name} - {self.amount}"


class Invoice(models.Model):
    """Student invoice"""
    
    class InvoiceStatus(models.TextChoices):
        DRAFT = 'draft', 'Draft'
        ISSUED = 'issued', 'Issued'
        PARTIAL = 'partial', 'Partially Paid'
        PAID = 'paid', 'Paid'
        OVERDUE = 'overdue', 'Overdue'
        CANCELLED = 'cancelled', 'Cancelled'
    
    id = models.UUIDField(primary_key=True, default=uuid.uuid4, editable=False)
    
    student = models.ForeignKey('students.Student', on_delete=models.CASCADE, related_name='invoices')
    semester = models.ForeignKey('university.Semester', on_delete=models.CASCADE, related_name='invoices')
    
    invoice_number = models.CharField(max_length=30, unique=True)
    
    # Amounts
    total_amount = models.DecimalField(max_digits=12, decimal_places=2)
    paid_amount = models.DecimalField(max_digits=12, decimal_places=2, default=Decimal('0.00'))
    balance = models.DecimalField(max_digits=12, decimal_places=2, default=Decimal('0.00'))
    
    status = models.CharField(max_length=20, choices=InvoiceStatus.choices, default=InvoiceStatus.DRAFT)
    
    # Issue date
    issue_date = models.DateField()
    due_date = models.DateField()
    
    # Discounts/Adjustments
    discount_amount = models.DecimalField(max_digits=12, decimal_places=2, default=Decimal('0.00'))
    adjustment_notes = models.TextField(blank=True)
    
    # Generated by
    generated_by = models.ForeignKey('students.User', on_delete=models.SET_NULL, null=True)
    
    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)
    
    class Meta:
        db_table = 'invoices'
        ordering = ['-created_at']
    
    def __str__(self):
        return f"{self.invoice_number} - {self.student.student_id}"
    
    def calculate_balance(self):
        self.balance = self.total_amount - self.paid_amount
        if self.balance <= 0:
            self.status = self.InvoiceStatus.PAID
        elif self.paid_amount > 0:
            self.status = self.InvoiceStatus.PARTIAL
        self.save()


